package fr.diginamic.builder;

class Categorie {

	String name;
	

	/** Constructor
	 * @param name
	 */
	public Categorie(String name) {
		super();
		this.name = name;
	}

	
	/** Getter for name
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/** Setter for name
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	
	
	

}
